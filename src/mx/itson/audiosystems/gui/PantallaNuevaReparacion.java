    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Reparacion reparacion = new Reparacion();
        reparacion.setNombreCliente(txtCliente.getText());
        reparacion.setNombreAparato(txtAparato.getText());
        reparacion.setFalla(txtFalla.getText());
        reparacion.setCostoTotal(Double.parseDouble(txtCostoTotal.getText()));
        reparacion.setAnticipo(Double.parseDouble(txtAnticipo.getText()));
        reparacion.setRestante(Double.parseDouble(txtRestante.getText()));
        reparacion.setFechaDeEntrega(txtFechaDeEntrega.getText());
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

